'''
Ejercicio 12: Calculadora de Área de un Rectángulo
Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la
longitud y el ancho del rectángulo.
'''

length = float(input('Introduzca el largo del rectángulo: '))
width = float(input('Introduzca el ancho del rectángulo: '))
operation = length * width
print(f'El área de las medidas introducidas del rectángulo es: {operation}')